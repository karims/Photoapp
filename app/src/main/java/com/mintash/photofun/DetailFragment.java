    private static final String ARG_PARAM1 = "param1";
    private static final String ARG_PARAM2 = "param2";

    // TODO: Rename and change types of parameters
    private String mParam1;
    private String mParam2;
    private Button saveButton;
    private OnFragmentInteractionListener mListener;
    private static final String KEY_URL = "picasso:url";
    /**
     * Use this factory method to create a new instance of
     * this fragment using the provided parameters.
     *
     * @param param1 Parameter 1.
     * @return A new instance of fragment DetailFragment.
     */
    // TODO: Rename and change types and number of parameters
    public static DetailFragment newInstance(String param1) {

        DetailFragment fragment = new DetailFragment();
        Bundle args = new Bundle();
        args.putString(KEY_URL, param1);
        fragment.setArguments(args);
        return fragment;
    }
    public DetailFragment() {
        // Required empty public constructor
    }

    @Override
    public void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        if (getArguments() != null) {
            mParam1 = getArguments().getString(ARG_PARAM1);
            mParam2 = getArguments().getString(ARG_PARAM2);
        }
    }

    @Override
    public View onCreateView(LayoutInflater inflater, ViewGroup container,
                             Bundle savedInstanceState) {
        // Inflate the layout for this fragment
        Activity activity = getActivity();

        View view = LayoutInflater.from(activity)
                .inflate(R.layout.fragment_detail, container, false);

        final FrameLayout frameLayout = new FrameLayout(activity);
        ImageView imageView = (ImageView) view.findViewById(R.id.photo);

        Bundle arguments = getArguments();
        String url = arguments.getString(KEY_URL);
        saveButton = new Button(activity);
        saveButton.setText("Save Image");
        imageView.setOnLongClickListener(new View.OnLongClickListener() {

            @Override
            public boolean onLongClick(View view) {
                Log.i("Longclick~~~", "long clicked");
                frameLayout.addView(saveButton);
                return false;
            }
        });

        Picasso.with(activity)
                .load(url)
                .fit()
                .into(imageView);

        return view;
    }

    // TODO: Rename method, update argument and hook method into UI event
    public void onButtonPressed(String uri) {
        if (mListener != null) {
            mListener.onFragmentInteraction(uri);
        }
    }

    @Override
    public void onAttach(Activity activity) {
        super.onAttach(activity);
        try {
            mListener = (OnFragmentInteractionListener) activity;
        } catch (ClassCastException e) {
            throw new ClassCastException(activity.toString()
                    + " must implement OnFragmentInteractionListener");
        }
    }

    @Override
    public void onDetach() {
        super.onDetach();
        mListener = null;
    }

